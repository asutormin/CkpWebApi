
namespace CkpModel.Input.Module
{
    public class ColorHolder
    {
        public int R { get; set; }
        public int G { get; set; }
        public int B { get; set; }

        public ColorHolder()
        { }

        public ColorHolder(int r, int g, int b)
        {
            R = r;
            G = g;
            B = b;
        }
    }
}
